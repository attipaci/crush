package crush.devel;



public class Counter {
	public double value = 0;
	
	public Counter() {}
	
	public Counter(double start) { value = start; }
	
	public void increment() { value++; }
	
	public void decrement() { value--; }
	
	public void reset() { value = 0; }

}
